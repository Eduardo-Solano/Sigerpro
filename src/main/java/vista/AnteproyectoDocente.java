    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
        if (proyectoSeleccionado == null) {
            JOptionPane.showMessageDialog(null, "Seleccione un anteproyecto primero.");
            return;
        }

        if (!proyectoSeleccionado.getDocenteProyectoCollection().isEmpty()) {
            for (DocenteProyecto dp : proyectoSeleccionado.getDocenteProyectoCollection()) {
                if ("Docente revisor".equalsIgnoreCase(dp.getRolDocente())) {
                    JOptionPane.showMessageDialog(null, "Este anteproyecto ya tiene un docente revisor asignado.");
                    return;
                }
            }
        }

        String docenteNombre = cboDocente.getSelectedIndex() > 0 ? (String) cboDocente.getSelectedItem() : null;

        if (docenteNombre == null) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un docente.");
            return;
        }

        // Buscar docente
        Docente docente = buscarDocentePorNombre(docenteNombre);

        // Verificar si ya está asignado con otro rol
        for (DocenteProyecto dp : proyectoSeleccionado.getDocenteProyectoCollection()) {
            if (dp.getDocente().getNoTarjeta().equals(docente.getNoTarjeta())) {
                JOptionPane.showMessageDialog(this, "Este docente ya está asignado a este proyecto con otro rol: " + dp.getRolDocente());
                return;
            }
        }

        // Guardar relación
        guardarRelacion(docente, proyectoSeleccionado, "Docente revisor");

        // Actualizar estado
        proyectoSeleccionado.setEstado("En revisión");
        Proyecto actualizado = controlProyecto.buscarProyectoPorId(proyectoSeleccionado.getIdProyecto());
        proyectoSeleccionado.setDocenteProyectoCollection(actualizado.getDocenteProyectoCollection());
        controlProyecto.editarProyecto(proyectoSeleccionado);

        // Refrescar tabla y cerrar
        bp.cargarTablaProyectos();
        JOptionPane.showMessageDialog(null, "Docente asignado correctamente.");
        limpiarFormulario();
        dispose();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Error al guardar: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        // TODO add your handling code here:
        if (!txtDescripcion.getText().isEmpty() || !txtAlumnos.getText().isEmpty()) {
            int opcion = JOptionPane.showConfirmDialog(null,
                    "Hay información cargada. ¿Seguro que desea cancelar?",
                    "Confirmación", JOptionPane.YES_NO_OPTION);
            if (opcion != JOptionPane.YES_OPTION) {
                return;
            }
        }
        limpiarFormulario();
        bp.cargarTablaProyectos();
        dispose(); // cerrar ventana
    }//GEN-LAST:event_btnCancelarActionPerformed

    private void cboAnteproyectoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboAnteproyectoActionPerformed
        // TODO add your handling code here:
        if (cargandoProyectos) {
            return;
        }

        String nombreProyecto = (String) cboAnteproyecto.getSelectedItem();
        if (nombreProyecto == null || nombreProyecto.trim().isEmpty()) {
            txtDescripcion.setText("");
            txtAlumnos.setText("");
            proyectoSeleccionado = null;
            return;
        }

        for (Proyecto p : controlProyecto.listarProyectos()) {
            if (p.getNombreProyecto().equals(nombreProyecto)) {
                proyectoSeleccionado = p;
                txtDescripcion.setText(p.getDescripcion());

                StringBuilder alumnos = new StringBuilder();
                for (Estudiante est : p.getEstudianteCollection()) {
                    Persona per = est.getIdPersona();
                    alumnos.append("Nombre: ").append(per.getNombre())
                            .append(" ").append(per.getApellidoP())
                            .append(" ").append(per.getApellidoM())
                            .append(" | No. Control: ").append(est.getNumControl()).append("\n");
                }
                txtAlumnos.setText(alumnos.toString());
                break;
            }
        }
    }//GEN-LAST:event_cboAnteproyectoActionPerformed

