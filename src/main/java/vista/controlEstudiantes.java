    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText().trim();
        String apP = txtApellidoP.getText().trim();
        String apM = txtApellidoM.getText().trim();
        String correo = txtCorreo.getText().trim();
        String control = txtControl.getText().trim();
        String semestre = txtSemestre.getText().trim();
        String departamento = "Ingenier√≠a en Sistemas Computacionales";

        String[] datos = {nombre, apP, apM, control, departamento, semestre};
        String errores = ValidadorCampos.validar(datos);

        if (errores != null) {
            JOptionPane.showMessageDialog(this, errores);
            return;
        }

        if (!correo.matches("^[\\w.-]+@[\\w.-]+\\.\\w{2,}$")) {
            JOptionPane.showMessageDialog(this, "Ingrese un correo electr√≥nico v√°lido.");
            return;
        }

        try {
            if (modoEdicion) {
                // üõ† Actualizar
                controlEstudiante.actualizarEstudianteFormulario(control, nombre, apP, apM, correo, semestre);
                JOptionPane.showMessageDialog(this, "Estudiante actualizado exitosamente.");
            } else {
                // ‚ûï Registrar
                Persona persona = new Persona();
                persona.setNombre(nombre);
                persona.setApellidoP(apP);
                persona.setApellidoM(apM);
                persona.setCorreo(correo);

                if (controlPersona.existePersona(persona)) {
                    JOptionPane.showMessageDialog(this, "La persona ya est√° registrada.");
                    return;
                }

                if (controlEstudiante.buscarPorNumeroControl(control) != null) {
                    JOptionPane.showMessageDialog(this, "El n√∫mero de control ya est√° registrado.");
                    return;
                }

                Departamento dep = controlDp.buscarDepartamentoPorNombre(departamento);
                if (dep == null) {
                    dep = new Departamento();
                    dep.setNombreDepartamento(departamento);
                    controlDp.guardarDepartamento(dep);
                }

                Expediente expediente = new Expediente();
                expediente.setNombreExpediente(control);
                expediente.setFechaCreacion(new java.util.Date());
                controlExpediente.guardarExpediente(expediente);

                Estudiante estudiante = new Estudiante();
                estudiante.setNumControl(control);
                estudiante.setSemestre(Integer.parseInt(semestre));
                estudiante.setIdDepartamento(dep);
                estudiante.setIdPersona(persona);
                estudiante.setIdExpediente(expediente);

                controlPersona.crearPersona(persona);
                controlEstudiante.guardarEstudiante(estudiante);
                JOptionPane.showMessageDialog(this, "Estudiante guardado exitosamente.");
            }

            cargarTablaEstudiantes();
            limpiarFormulario();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al guardar/actualizar el estudiante.");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

