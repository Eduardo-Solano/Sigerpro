    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int fila = tableEstudiantes.getSelectedRow();
        if (fila >= 0) {
            String numControl = tableEstudiantes.getValueAt(fila, 0).toString();
            Estudiante e = controlEstudiante.buscarPorNumeroControl(numControl);
            if (e == null || e.getIdPersona() == null) {
                JOptionPane.showMessageDialog(this, "No se encontr√≥ el estudiante seleccionado.");
                return;
            }
            Persona p = e.getIdPersona();
            txtControl.setText(e.getNumControl());
            txtNombre.setText(p.getNombre());
            txtApellidoP.setText(p.getApellidoP());
            txtApellidoM.setText(p.getApellidoM());
            txtCorreo.setText(p.getCorreo());
            txtSemestre.setText(String.valueOf(e.getSemestre()));
            txtControl.setEnabled(false); // No permitir cambiar el n√∫mero de control
            modoEdicion = true; // Activar modo edici√≥n
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un estudiante de la tabla antes de editar.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

