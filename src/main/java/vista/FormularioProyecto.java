    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String nombreProyecto = txtNombre.getText();
        String descripcion = txtDescripcion.getText();
        String numAlumnosSugeridosStr = (String) jComboSugeridos.getSelectedItem();
        String nombreEmpresaSeleccionada = (String) jComboEmpresas.getSelectedItem();

        String nombreEmpresa = "";
        Empresa empresaSeleccionada = null;
        final int MAX_NOMBRE_LENGTH = 254; 
        final int MAX_DESCRIPCION_LENGTH = 254; 
        
        if (nombreProyecto.length() > MAX_NOMBRE_LENGTH) {
        JOptionPane.showMessageDialog(this, "El nombre del proyecto no puede exceder los " + MAX_NOMBRE_LENGTH + " caracteres.", "Error de Validaci√≥n", JOptionPane.WARNING_MESSAGE);
        return;
    }

if (descripcion.length() > MAX_DESCRIPCION_LENGTH) {
    JOptionPane.showMessageDialog(this, "La descripci√≥n no puede exceder los " + MAX_DESCRIPCION_LENGTH + " caracteres.", "Error de Validaci√≥n", JOptionPane.WARNING_MESSAGE);
    return;
}


        if (descripcion.length() > MAX_DESCRIPCION_LENGTH) {
        JOptionPane.showMessageDialog(this, "La descripci√≥n no puede exceder los " + MAX_DESCRIPCION_LENGTH + " caracteres.", "Error de Validaci√≥n", JOptionPane.WARNING_MESSAGE);
        txtDescripcion.setText("");
        txtNombre.setText("");
        return;
        }
        


        if (nombreEmpresaSeleccionada != null) {
            if (nombreEmpresaSeleccionada.equals("No hay empresas registradas") && !jComboEmpresas.isEnabled()) {
                JOptionPane.showMessageDialog(this, "No hay empresas disponibles para seleccionar.", "Error", JOptionPane.WARNING_MESSAGE);
                return;
            }

            // Buscar empresa usando el controlador
            ControladoraEmpresa controlEmpresa = new ControladoraEmpresa();
            empresaSeleccionada = controlEmpresa.buscarEmpresaPorNombre(nombreEmpresaSeleccionada);
            System.out.println(empresaSeleccionada.getNombreEmpresa());

            if (empresaSeleccionada != null) {
                nombreEmpresa = empresaSeleccionada.getNombreEmpresa();
            } else {
                JOptionPane.showMessageDialog(this, "La empresa seleccionada no fue encontrada. Por favor, recargue la lista.", "Error de Datos", JOptionPane.ERROR_MESSAGE);
                return;
            }

        } else {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una empresa.", "Error de Selecci√≥n", JOptionPane.WARNING_MESSAGE);
            return;
        }

// Estado y documento por defecto
        String estadoProyecto = "Sin Asignar";
        String urlDocumento = "N/A";

        try {
            // Validar campos obligatorios
            if (nombreProyecto.isEmpty() || descripcion.isEmpty() || numAlumnosSugeridosStr == null
                    || numAlumnosSugeridosStr.isEmpty() || nombreEmpresa.isEmpty() ) {
                JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios.", "Error de Validaci√≥n", JOptionPane.WARNING_MESSAGE);
                return;
            }

            Integer numAlumnS = Integer.parseInt(numAlumnosSugeridosStr);
            Date fechaActual = new Date();

            Proyecto nuevoProyecto = new Proyecto();
            nuevoProyecto.setNombreProyecto(nombreProyecto);
            nuevoProyecto.setDescripcion(descripcion);
            nuevoProyecto.setEstado(estadoProyecto);
            nuevoProyecto.setUrlDocumento(urlDocumento);
            nuevoProyecto.setNumAlumnosSug(numAlumnS);
            nuevoProyecto.setFechaRegistro(fechaActual);
            nuevoProyecto.setIdEmpresa(empresaSeleccionada);
            controladoraProyecto.guardarProyecto(nuevoProyecto);

            JOptionPane.showMessageDialog(this, "Proyecto registrado exitosamente.", "Registro Exitoso", JOptionPane.INFORMATION_MESSAGE);
            limpiarCampos();
            cargarEmpresasEnComboBox();
            bp.cargarTablaProyectos();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El n√∫mero de alumnos sugeridos debe ser un n√∫mero entero v√°lido.", "Error de Formato", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ocurri√≥ un error inesperado al registrar el proyecto: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        }//GEN-LAST:event_btnRegistrarActionPerformed

    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
    String textoActual = txtNombre.getText();  // ‚Üê Aqu√≠ estaba el error, usabas txtDescripcion
    final int MAX_LENGTH = 254;

    // Permitir retroceso y suprimir
    if (evt.getKeyChar() == KeyEvent.VK_BACK_SPACE || evt.getKeyChar() == KeyEvent.VK_DELETE) {
        return;
    }

    // Permitir letras y espacios
    if (!Character.isLetter(evt.getKeyChar()) && !Character.isSpaceChar(evt.getKeyChar())) {
        evt.consume();
        return;
    }

    // Evitar escribir m√°s de 254 caracteres
    if (textoActual.length() >= MAX_LENGTH) {
        evt.consume();
    }

    }//GEN-LAST:event_txtNombreKeyTyped

