    private void inicializarFormulario() {
        cargandoProyectos = true;

        cboAnteproyecto.removeAllItems();
        for (Proyecto p : controlProyecto.listarProyectos()) {
            if ("Asignado".equalsIgnoreCase(p.getEstado())) {
                cboAnteproyecto.addItem(p.getNombreProyecto());
            }
        }
        cboAnteproyecto.setSelectedIndex(-1); // No seleccionar por defecto

        cboDocente.removeAllItems();
        cboDocente.addItem("-- Selecciona un docente --");
        for (Docente d : controlDocente.listarDocentes()) {
            String nombreCompleto = d.getIdPersona().getNombre() + " "
                    + d.getIdPersona().getApellidoP() + " "
                    + d.getIdPersona().getApellidoM();
            cboDocente.addItem(nombreCompleto);
        }

        txtAlumnos.setText("");
        txtDescripcion.setText("");

        txtAlumnos.setEditable(false);
        txtDescripcion.setEditable(false);
        cboAnteproyecto.setEditable(false);
        cboDocente.setEditable(false);

        cargandoProyectos = false;
    }

    private Docente buscarDocentePorNombre(String nombreCompleto) {
        for (Docente d : controlDocente.listarDocentes()) {
            String nombre = d.getIdPersona().getNombre() + " "
                    + d.getIdPersona().getApellidoP() + " "
                    + d.getIdPersona().getApellidoM();
            if (nombre.equals(nombreCompleto)) {
                return d;
            }
        }
        return null;
    }

    private void guardarRelacion(Docente docente, Proyecto proyecto, String rol) throws Exception {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("SigerproMVC");
        DocenteProyectoJpaController controlPD = new DocenteProyectoJpaController(emf);

        DocenteProyectoPK pk = new DocenteProyectoPK(proyecto.getIdProyecto(), docente.getNoTarjeta());
        DocenteProyecto existente = controlPD.findDocenteProyecto(pk);

        if (existente != null) {
            throw new Exception("El docente ya está asignado a este proyecto con otro rol: " + existente.getRolDocente());
        }

        DocenteProyecto pd = new DocenteProyecto();
        pd.setDocenteProyectoPK(pk);
        pd.setDocente(docente);
        pd.setProyecto(proyecto);
        pd.setRolDocente(rol);
        controlPD.create(pd);
    }

    private void limpiarFormulario() {
        cboAnteproyecto.setSelectedIndex(-1);
        cboDocente.setSelectedIndex(0);
        txtAlumnos.setText("");
        txtDescripcion.setText("");
        proyectoSeleccionado = null;
    }

