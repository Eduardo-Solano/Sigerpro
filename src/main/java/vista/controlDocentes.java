    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String texto = txtBuscar.getText().trim().toLowerCase();
        if (texto.isEmpty() || texto.equals("Buscar docente...")) {
            JOptionPane.showMessageDialog(this, "Ingrese un nombre o número de tarjeta para buscar.");
            return;
        }

        DefaultTableModel modelo = (DefaultTableModel) tableDocentes.getModel();
        modelo.setRowCount(0);

        for (Docente d : controlDocente.listarDocentes()) {
            Persona p = d.getIdPersona();
            String completo = (p.getNombre() + " " + p.getApellidoP() + " " + p.getApellidoM()).toLowerCase();
            if (d.getNoTarjeta().contains(texto) || p.getNombre().toLowerCase().contains(texto)
                    || p.getApellidoP().toLowerCase().contains(texto) || p.getApellidoM().toLowerCase().contains(texto)
                    || completo.contains(texto)) {
                modelo.addRow(new Object[]{d.getNoTarjeta(), p.getNombre(), p.getApellidoP(), p.getApellidoM(), p.getCorreo(), d.getIdDepartamento().getNombreDepartamento()});
            }
        }

        JOptionPane.showMessageDialog(this, "Mostrando resultados para: " + texto);
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        // TODO add your handling code here:
        ArrayList<Persona> personas = new ArrayList<>();
        ArrayList<Docente> docentes = new ArrayList<>();
        DefaultTableModel dummy = new DefaultTableModel();

        try {
            new CargarCSV().subirCSVDocentes(personas, docentes, dummy);
            controlDocente.guardarDocentesCSV(personas, docentes);
            cargarTablaDocentes();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al cargar el archivo CSV.");
        }
    }//GEN-LAST:event_btnCargarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText().trim();
        String apP = txtApellidoP.getText().trim();
        String apM = txtApellidoM.getText().trim();
        String correo = txtCorreo.getText().trim();
        String tarjeta = txtTarjeta.getText().trim();

        String[] datos = {nombre, apP, apM, tarjeta};
        String errores = ValidadorCampos.validarD(datos);

        if (errores != null) {
            JOptionPane.showMessageDialog(this, errores);
            return;
        }

        if (!correo.matches("^[\\w.-]+@[\\w.-]+\\.\\w{2,}$")) {
            JOptionPane.showMessageDialog(this, "Ingrese un correo electrónico válido.");
            return;
        }

        try {
            if (modoEdicion) {
                //  MODO EDICIÓN
                controlDocente.actualizarDocenteFormulario(tarjeta, nombre, apP, apM, correo);
                JOptionPane.showMessageDialog(this, "Docente actualizado exitosamente.");
            } else {
                //  MODO REGISTRO NUEVO
                controlDocente.guardarDocenteFormulario(nombre, apP, apM, tarjeta, correo, "Ingeniería en Sistemas Computacionales");
                JOptionPane.showMessageDialog(this, "Docente guardado exitosamente.");
            }

            cargarTablaDocentes();
            limpiarFormulario(); // Reinicia el formulario
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al guardar/actualizar el docente. Intenta nuevamente.");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

