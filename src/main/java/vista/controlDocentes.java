    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int fila = tableDocentes.getSelectedRow();
        if (fila >= 0) {
            String tarjeta = tableDocentes.getValueAt(fila, 0).toString();
            Docente d = controlDocente.buscarPorNumeroTarjeta(tarjeta);
            if (d == null || d.getIdPersona() == null) {
                JOptionPane.showMessageDialog(this, "No se encontró el docente seleccionado.");
                return;
            }
            Persona p = d.getIdPersona();
            txtTarjeta.setText(d.getNoTarjeta());
            txtNombre.setText(p.getNombre());
            txtApellidoP.setText(p.getApellidoP());
            txtApellidoM.setText(p.getApellidoM());
            txtCorreo.setText(p.getCorreo());
            txtTarjeta.setEnabled(false); // No permitir editar tarjeta
            modoEdicion = true; // 🚨 ACTIVAR modo edición
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un docente de la tabla antes de editar.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

