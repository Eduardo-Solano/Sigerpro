    private void cargarTablaDocentes() {
        DefaultTableModel modelo = (DefaultTableModel) tableDocentes.getModel();
        modelo.setRowCount(0);
        try {
            List<Docente> lista = controlDocente.listarDocentes();
            for (Docente d : lista) {
                Persona p = d.getIdPersona();
                if (p != null) {
                    modelo.addRow(new Object[]{
                        d.getNoTarjeta(), p.getNombre(), p.getApellidoP(), p.getApellidoM(), p.getCorreo(), d.getIdDepartamento().getNombreDepartamento()
                    });
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar docentes. Intenta nuevamente.");
        }
    }

