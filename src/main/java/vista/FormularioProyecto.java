    private void cargarEmpresasEnComboBox() {
        List<Empresa> empresas = controladoraProyecto.obtenerTodasLasEmpresas();

        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();

        if (empresas != null && !empresas.isEmpty()) {
            for (Empresa empresa : empresas) {
                model.addElement(empresa.getNombreEmpresa());
            }
            jComboEmpresas.setModel(model);
            jComboEmpresas.setEnabled(true);

            if (jComboEmpresas.getItemCount() > 0) {
                jComboEmpresas.setSelectedIndex(0);
            }
        } else {
            DefaultComboBoxModel<String> emptyModel = new DefaultComboBoxModel<>();
            emptyModel.addElement("No hay empresas registradas");

            jComboEmpresas.setModel(emptyModel);
            jComboEmpresas.setEnabled(false);
        }

    }

    private void cargarSugeridosEnComboBox() {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        for (int i = 1; i <= 4; i++) {
            model.addElement(String.valueOf(i));
        }
        jComboSugeridos.setModel(model);
        jComboSugeridos.setSelectedIndex(0);
    }

    private void limpiarCampos() {
        txtNombre.setText("");
        txtDescripcion.setText("");
        if (jComboSugeridos.getItemCount() > 0) {
            jComboSugeridos.setSelectedIndex(0);
        }
        if (jComboEmpresas.getItemCount() > 0) {
            jComboEmpresas.setSelectedIndex(0);
        }
       // txtNumeroContacto.setText("");
    }

