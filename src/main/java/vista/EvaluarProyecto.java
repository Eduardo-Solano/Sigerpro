        private void configurarVentana() {
        // Agrupar los JCheckBox para que solo uno pueda ser seleccionado a la vez
        ButtonGroup grupoEvaluacion = new ButtonGroup();
        grupoEvaluacion.add(jCheckAprovado); // Aprobado
        grupoEvaluacion.add(jCheckCorreccion);// Corrección
        grupoEvaluacion.add(jCheckRechazado); // Rechazado

        // Opcional: Deseleccionar todos al inicio
        grupoEvaluacion.clearSelection();

        // Configurar el título de la ventana o algún campo para mostrar el nombre del proyecto
        if (proyectoAevaluar != null) {
            this.setTitle("Evaluar Proyecto: " + proyectoAevaluar.getNombreProyecto());
            // Si tuvieras un JLabel para el nombre del proyecto en esta ventana, podrías usarlo:
            // lblNombreProyecto.setText("Proyecto: " + proyectoAevaluar.getNombreProyecto());
        } else {
            this.setTitle("Evaluar Proyecto");
        }

        // Deshabilitar el botón Guardar inicialmente hasta que una opción sea seleccionada
        jButtonGuardar.setEnabled(false);

        // Añadir listeners a los checkboxes para habilitar el botón Guardar
        ActionListener checkboxListener = e -> jButtonGuardar.setEnabled(jCheckAprovado.isSelected() ||jCheckCorreccion.isSelected() || jCheckRechazado.isSelected());
        jCheckAprovado.addActionListener(checkboxListener);
        jCheckCorreccion.addActionListener(checkboxListener);
        jCheckRechazado.addActionListener(checkboxListener);

        // Añadir ActionListener para el botón Guardar
        jButtonGuardar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                jButtonGuardarActionPerformed(evt);
            }
        });
    }
    
