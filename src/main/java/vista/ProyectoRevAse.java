    private void inicializarFormulario() {
        cargandoProyectos = true; // ← Evita que se dispare el listener al cargar

        cboProyecto.removeAllItems();
        for (Proyecto p : controlProyecto.listarProyectos()) {
            if ("Aprobado".equalsIgnoreCase(p.getEstado())) {
                cboProyecto.addItem(p.getNombreProyecto());
            }
        }
        cboProyecto.setSelectedIndex(-1); // Sin selección inicial

        cboAsesor.removeAllItems();
        cboRevisor1.removeAllItems();
        cboRevisor2.removeAllItems();

        cboAsesor.addItem("-- Selecciona un docente --");
        cboRevisor1.addItem("-- Selecciona un docente --");
        cboRevisor2.addItem("-- Selecciona un docente --");

        for (Docente d : controlDocente.listarDocentes()) {
            String nombreCompleto = d.getIdPersona().getNombre() + " "
                    + d.getIdPersona().getApellidoP() + " "
                    + d.getIdPersona().getApellidoM();
            cboAsesor.addItem(nombreCompleto);
            cboRevisor1.addItem(nombreCompleto);
            cboRevisor2.addItem(nombreCompleto);
        }

        txtAlumnos.setText("");
        txtDescripcion.setText("");

        txtAlumnos.setEditable(false);
        txtDescripcion.setEditable(false);
        cboProyecto.setEditable(false);
        cboAsesor.setEditable(false);
        cboRevisor1.setEditable(false);
        cboRevisor2.setEditable(false);

        cargandoProyectos = false; // ← Reactiva el listener
    }

    private boolean docenteYaAsignado(Proyecto proyecto, Docente docente) {
    for (DocenteProyecto dp : proyecto.getDocenteProyectoCollection()) {
        if (dp.getDocente().getNoTarjeta().equals(docente.getNoTarjeta())) {
            return true;
        }
    }
    return false;
}

