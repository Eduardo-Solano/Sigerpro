    private void cargarTablaEstudiantes() {
        DefaultTableModel modelo = (DefaultTableModel) tableEstudiantes.getModel();
        modelo.setRowCount(0);
        try {
            List<Estudiante> lista = controlEstudiante.listarEstudiantes();
            for (Estudiante e : lista) {
                Persona p = e.getIdPersona();
                if (p != null) {
                    modelo.addRow(new Object[]{
                        e.getNumControl(), p.getNombre(), p.getApellidoP(), p.getApellidoM(), p.getCorreo(), e.getSemestre(), e.getIdDepartamento().getNombreDepartamento()
                    });
                }
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al cargar estudiantes. Intenta nuevamente.");
        }
    }

