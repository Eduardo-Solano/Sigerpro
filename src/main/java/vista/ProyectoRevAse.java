    private void cboProyectoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboProyectoActionPerformed
        // TODO add your handling code here:
        if (cargandoProyectos) {
            return; // ← Bloquea si está inicializando
        }
        String nombreProyecto = (String) cboProyecto.getSelectedItem();
        if (nombreProyecto == null || nombreProyecto.trim().isEmpty()) {
            txtDescripcion.setText("");
            txtAlumnos.setText("");
            proyectoSeleccionado = null;
            return;
        }

        for (Proyecto p : controlProyecto.listarProyectos()) {
            if (p.getNombreProyecto().equals(nombreProyecto)) {
                proyectoSeleccionado = p;
                txtDescripcion.setText(p.getDescripcion());

                StringBuilder alumnos = new StringBuilder();
                for (Estudiante est : p.getEstudianteCollection()) {
                    Persona per = est.getIdPersona();
                    alumnos.append("Nombre: ").append(per.getNombre())
                            .append(" ").append(per.getApellidoP())
                            .append(" ").append(per.getApellidoM())
                            .append(" | No. Control: ").append(est.getNumControl()).append("\n");
                }
                txtAlumnos.setText(alumnos.toString());
                break;
            }
        }
    }//GEN-LAST:event_cboProyectoActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        try {
        if (proyectoSeleccionado == null) {
            JOptionPane.showMessageDialog(this, "Seleccione un proyecto primero.");
            return;
        }

        String asesor = (String) cboAsesor.getSelectedItem();
        String rev1 = (String) cboRevisor1.getSelectedItem();
        String rev2 = (String) cboRevisor2.getSelectedItem();

        if (asesor == null || rev1 == null || asesor.contains("Selecciona") || rev1.contains("Selecciona")) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar al menos un asesor y un revisor.");
            return;
        }

        if (asesor.equals(rev1) || (rev2 != null && (asesor.equals(rev2) || rev1.equals(rev2)))) {
            JOptionPane.showMessageDialog(this, "No puede asignar el mismo docente en más de un rol.");
            return;
        }

        Docente docAsesor = buscarDocentePorNombre(asesor);
        Docente docRev1 = buscarDocentePorNombre(rev1);
        Docente docRev2 = (rev2 != null && !rev2.contains("Selecciona")) ? buscarDocentePorNombre(rev2) : null;

        // Validar duplicidad de rol
        if (docenteYaAsignado(proyectoSeleccionado, docAsesor) ||
            docenteYaAsignado(proyectoSeleccionado, docRev1) ||
            (docRev2 != null && docenteYaAsignado(proyectoSeleccionado, docRev2))) {
            JOptionPane.showMessageDialog(this, "Uno o más docentes ya están asignados al proyecto con otro rol.");
            return;
        }

        guardarRelacion(docAsesor, proyectoSeleccionado, "Asesor");
        guardarRelacion(docRev1, proyectoSeleccionado, "Revisor");
        if (docRev2 != null) {
            guardarRelacion(docRev2, proyectoSeleccionado, "Revisor");
        }

        JOptionPane.showMessageDialog(this, "Docentes asignados correctamente.");

        // Selección de fechas
        java.time.LocalDate hoy = java.time.LocalDate.now();
        java.time.format.DateTimeFormatter formatter = java.time.format.DateTimeFormatter.ofPattern("dd/MM/yyyy");
        java.time.LocalDate[] fechas = {
            hoy, hoy.plusDays(1), hoy.plusDays(2), hoy.plusDays(3)
        };
        String[] opciones = new String[fechas.length];
        for (int i = 0; i < fechas.length; i++) {
            opciones[i] = fechas[i].format(formatter);
        }

        int seleccion = -1;
        while (seleccion < 0) {
            seleccion = JOptionPane.showOptionDialog(
                    this,
                    "Selecciona la fecha de inicio de residencia para este proyecto:",
                    "Establecer fecha de inicio",
                    JOptionPane.DEFAULT_OPTION,
                    JOptionPane.QUESTION_MESSAGE,
                    null,
                    opciones,
                    opciones[0]);
        }

        java.util.Date dateInicio = java.util.Date.from(fechas[seleccion].atStartOfDay(java.time.ZoneId.systemDefault()).toInstant());
        java.util.Date dateFin = java.util.Date.from(fechas[seleccion].plusWeeks(18).atStartOfDay(java.time.ZoneId.systemDefault()).toInstant());

        Proyecto proyBD = controlProyecto.buscarProyectoPorId(proyectoSeleccionado.getIdProyecto());
        proyBD.setFechaInicio(dateInicio);
        proyBD.setFechaFin(dateFin);
        controlProyecto.editarProyecto(proyBD);

        // Refrescar tabla y cerrar
        JOptionPane.showMessageDialog(this, "Fechas guardadas correctamente.");
        limpiarFormulario();
        new BancoDeProyectos().cargarTablaProyectos();
        dispose();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this, "Error al guardar: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_btnGuardarActionPerformed

