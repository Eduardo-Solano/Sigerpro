    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        if (proyectoAevaluar == null) {
            JOptionPane.showMessageDialog(this, "Error: No se ha cargado un proyecto para evaluar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String nuevoEstado = "";
            if (jCheckAprovado.isSelected()) {
            nuevoEstado = "Aprobado";
        } else if (jCheckCorreccion.isSelected()){ 
            nuevoEstado = "En corrección";
        } else if (jCheckRechazado.isSelected()) {
            nuevoEstado = "Rechazado";
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione si el proyecto fue Aprobado o Rechazado.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Confirmación antes de guardar
        int confirm = JOptionPane.showConfirmDialog(this,
                "¿Está seguro de que desea cambiar el estado del proyecto '" + proyectoAevaluar.getNombreProyecto() + "' a '" + nuevoEstado + "'?",
                "Confirmar Evaluación",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                ControladoraProyecto controladora = new ControladoraProyecto();
                proyectoAevaluar.setEstado(nuevoEstado);
                controladora.editarProyecto(proyectoAevaluar); // Suponiendo que tienes un método editarProyecto

                JOptionPane.showMessageDialog(this, "El proyecto ha sido evaluado como: " + nuevoEstado, "Éxito", JOptionPane.INFORMATION_MESSAGE);

                // Refrescar la tabla en BancoDeProyectos si la referencia no es nula
                if (bancoDeProyectos != null) {
                    bancoDeProyectos.cargarTablaProyectos(); // Recargar la tabla principal
                }

                a.animarSalida(this); // Animar salida
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Error al guardar la evaluación del proyecto: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace(); 
            }
        }    }//GEN-LAST:event_jButtonGuardarActionPerformed
    
    
    
